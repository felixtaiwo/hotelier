    private final UserService userService;
    private final PasswordEncoder encoder;
    private final PersonnelRepo personnelRepo;
    Logger logger = Logger.getLogger(this.getClass().getName());

    public TokenDto generateJwt(User user) {
        Date expiry = new Date(new Date().getTime() + 999999999);
        String token = Jwts.builder()
                .setSubject(user.getEmailAddress())
                .setIssuedAt(new Date())
                .setExpiration(expiry)
                .signWith(SignatureAlgorithm.HS512, getJwtKey())
                .compact();
        LocalDateTime tokenExpiry = expiry.toInstant()
                .atZone(ZoneId.of("Africa/Lagos"))
                .toLocalDateTime();
        return new TokenDto(UserDto.fromUser(user), token, tokenExpiry);
    }

    public TokenDto refreshToken(String token) {
        String username = getUserNameFromJwtToken(token);
        User user = userService.getUserByEmailAddress(username);
        return generateJwt(user);
    }

    public UserDto getUserByToken(String token) {
        String username = getUserNameFromJwtToken(token);
        return userService.getUserDtoByEmailAddress(username);
    }

    public TokenDto login(LoginDto loginDto) {
        User user = userService.getUserByEmailAddress(loginDto.getEmailAddress());
        if (encoder.matches(loginDto.getPassword(), user.getPassword())) {
            return generateJwt(user);
        }
        throw new Hotelier(HttpStatus.UNAUTHORIZED, "Bad credentials");
    }


    public boolean validateJwtToken(String authToken) {
        try {
            Jwts.parser().setSigningKey(getJwtKey()).parseClaimsJws(authToken);
            return true;
        } catch (SignatureException e) {
            logger.log(Level.SEVERE, "Invalid JWT signature: {}", e.getMessage());
        } catch (MalformedJwtException e) {
            logger.log(Level.SEVERE, "Invalid JWT token: {}", e.getMessage());
        } catch (ExpiredJwtException e) {
            logger.log(Level.SEVERE, "JWT token is expired: {}", e.getMessage());
        } catch (UnsupportedJwtException e) {
            logger.log(Level.SEVERE, "JWT token is unsupported: {}", e.getMessage());
        } catch (IllegalArgumentException e) {
            logger.log(Level.SEVERE, "JWT claims string is empty: {}", e.getMessage());
        }

        return false;
    }



    @Bean
    @RequestScope
    public Personnel getLoggedInPersonnel() {
        Personnel personnel = null;
        User user = userContext();
        if (user != null && user.getRoles().stream().anyMatch(role -> role.getTitle() == SystemRoles.ADMIN || role.getTitle() == SystemRoles.SUPER_ADMIN)) {
            personnel = personnelRepo.findByUser(user);
        }
        return personnel;
    }

    public User userContext() {
        Authentication jwt = SecurityContextHolder.getContext().getAuthentication();
        String subject = jwt.getName();
        if (subject != null) {
            logger.info("Logged in user information: " + subject);
            try {
                return userService.getUserByEmailAddress(subject);
            } catch (Hotelier e) {
                return null;
            }
        }
        return null;

    }

    public String getUserNameFromJwtToken(String token) {
        return Jwts.parser().setSigningKey(getJwtKey()).parseClaimsJws(token).getBody().getSubject();
    }

